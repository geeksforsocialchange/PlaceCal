  def view_template
    config = VARIANTS.fetch(@variant)
    return if @variant == :adding_events && !@site&.site_admin

    div(class: 'text-center mb-12 [&_h3]:my-8 [&_h3_span]:border-b-[3px] [&_ul]:list-none [&_ul]:p-0 [&_ol]:list-none [&_ol]:p-0') do
      raw(safe(SVGS.fetch(@variant)))

      h3(class: 'h2--alt') { span { config[:title] } }
      p { config[:description] } if config[:description]

      if @variant == :adding_events
        mail_to(@site.site_admin.email, 'Email now', class: 'btn btn--lg')
      elsif config[:link]
        link_to(config[:link][:text], config[:link][:url], class: 'btn btn--lg')
      elsif @places
        render_places_list(config[:empty_message])
      end
    end
  end

  private

  def render_places_list(empty_message)
    if @places.any?
      ul do
        @places.each do |location|
          li { link_to(location.name, partner_path(location)) }
        end
      end
    else
      p { empty_message }
    end
  end
